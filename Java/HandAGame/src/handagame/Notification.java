package handagame;
import java.util.Date;

/**
 *
 * @author Amaro
 */
public class Notification {
    private String text;
    private Date date;
    private int ID;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }
}
